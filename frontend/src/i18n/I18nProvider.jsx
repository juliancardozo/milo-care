const I18nContext = createContext(null);

function getNested(obj, path) {
  return path.split('.').reduce((acc, key) => (acc ? acc[key] : undefined), obj);
}

function interpolate(template, values) {
  if (!values) return template;
  return Object.entries(values).reduce(
    (acc, [key, value]) => acc.replaceAll(`{${key}}`, String(value)),
    template,
  );
}

export function I18nProvider({ children }) {
  const [language, setLanguage] = useState(localStorage.getItem(STORAGE_KEY) || 'es');

  const value = useMemo(() => ({
    language,
    setLanguage: (lang) => {
      localStorage.setItem(STORAGE_KEY, lang);
      setLanguage(lang);
    },
    t: (key, values) => {
      const current = getNested(translations[language], key);
      const fallback = getNested(translations.en, key);
      const result = current ?? fallback ?? key;
      return typeof result === 'string' ? interpolate(result, values) : result;
    },
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
}